package day04;

public class P01_Operatorler {
    public static void main(String[] args) {

        /*
        +: toplama
        -: cikarma
        *: carpma
        /: bolme

        ||: veya
        &&: ve
        %: modulus

        increment ve decrement bir artirma ve bir azaltmayi ifade eder
         */

        //increment
        int a=5;
        System.out.println(a);//5

        a=a+5;
        System.out.println(a);//10

        a+=5;
        System.out.println(a);//15
    }
}
